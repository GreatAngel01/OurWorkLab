package ru.vsu.app.help;

public class Coordinate {
    private int x;
    private int y;

    public Coordinate(int y , int x) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
